void main(List<String> args) {
  // BASIC DART VARIABLES
  var name = "Voyager I";
  var year = 1997;
  var antennaDiameter = 3.7;

  print("name: ${name}, year: ${year}, antena diameter: ${antennaDiameter}");

  // OPERATORS
  int x = 10;
  int y = 10;
  var z = x + y; //Addition
  print(z);

  print(x - y); // Substraction
  print(x * y); // Multiply
  print(x / y); // divide
  print(x ~/ y); // divide and return integer result
  print(x % y); // modulo(get the remainder)
  print(x++); // increment by +1
  print(x--); // decrement by -1
}
